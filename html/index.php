                    <span style="color: darkred ; font-size:40px; " > Wine </span>(from Latin vinum) is an alcoholic beverage made from fermented grapes or other fruits. 
                    Due to the natural chemical balance, grapes ferment without the addition of sugars, acids, enzymes, water, 
                    or other nutrients.Yeast consumes the sugar in the grapes and converts it to ethanol and carbon dioxide.
                    Different varieties of grapes and strains of yeasts produce different styles of wine. 
                    The well-known variations result from the very complex interactions between the biochemical 
                    development of the fruit, reactions involved in fermentation, the terroir (the special characteristics 
                    imparted by geography, geology, climate and plant genetics) and subsequent appellation (the legally defined 
                    and protected geographical indication used to identify where the grapes for a wine were grown), along with human intervention in the overall process. 
