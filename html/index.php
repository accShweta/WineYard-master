               <h1> Breif History of Wine </h1>
                <p> The peoples of the Mediterranean began to emerge from barbarism when they learned to cultivate the olive and the vine." So wrote the Greek historian Thucydides in the fifth century B.C.,and indeed,wine-making is as old as civilization itself. Just as society finds its roots in ancient Mesopotamia, the earliest evidence we have for the cultivation of grapes and the supervised fermentation of their juices dates back to 6000 B.C. in the ancient Middle East. The Egyptians recorded the harvest of grapes on the walls of their tombs; bottles of wine were even buried with pharaohs in order that they might entertain guests in the afterlife. Wine was also considered a drink of the elite in ancient Greece, and it was a centerpiece of the famous symposia, immortalized by Plato and the poets of the period. But it was during the Roman era that wine became popular throughout society. In Roman cities wine bars were set up on almost every street, and the Romans exported wine and wine-making to the rest of Europe. Soon, production and quality of wine in other regions rivaled that of Rome herself: in A.D. 92, Emperor Domitian decreed that all of the vines in the Cahors region (near Bordeaux) be pulled out, ostensibly in favor of the wheat cultivation the empire so desperately needed, but possibly also to quell the competition with Italian wine exports.
